async function seed() {
  console.log(`Seeding ${PLANTS.length} plants…`);

  // Delete existing plants
  const { error: delError } = await supabase.from('qj_plante').delete().gte('ordre', 0);
  if (delError) {
    console.error('Delete error:', delError.message);
    process.exit(1);
  }

  // Insert all plants
  const rows = PLANTS.map(p => ({
    nom: p.nom,
    sous_titre: p.sous_titre ?? null,
    nom_latin: p.nom_latin ?? null,
    famille: p.famille ?? null,
    description: p.intro ?? null,
    intro: p.intro ?? null,
    sens: p.sens ?? null,
    vertus: p.vertus ?? null,
    contre_indications: p.contre_indications ?? null,
    vignes: p.vignes ?? null,
    proprietes: [],
    image_url: p.image_url ?? null,
    audio_url: p.audio_url ?? null,
    ordre: p.ordre,
  }));

  const { error } = await supabase.from('qj_plante').insert(rows);
  if (error) {
    console.error('Insert error:', error.message);
    process.exit(1);
  }

  console.log('Done!');
}

